package GeneticTSP;

/**
 * Description: 城市的坐标
 * Author: Quan Tang
 * Date: 2018/12/20
 **/
public class Position {
    int x;
    int y;

    Position() {
        this.x = 0;
        this.y = 0;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
